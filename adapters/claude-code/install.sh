#!/usr/bin/env bash
# Install AgentCloseoutBench physics-backed Claude Code hook adapters.

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
TARGET="${1:-}"

if [ -z "$TARGET" ] || [ "$TARGET" = "-h" ] || [ "$TARGET" = "--help" ]; then
  echo "Usage: bash adapters/claude-code/install.sh /path/to/project [hook ...]" >&2
  echo "Hooks: no-vibes no-wrap-up no-cliffhanger no-roleplay-drift no-sycophancy" >&2
  echo "A PreToolUse tamper guard is installed with every selection." >&2
  exit 2
fi

shift || true

ALL_HOOKS=(no-vibes no-wrap-up no-cliffhanger no-roleplay-drift no-sycophancy)
HOOKS=("$@")
if [ "${#HOOKS[@]}" -eq 0 ]; then
  HOOKS=("${ALL_HOOKS[@]}")
fi

case "$TARGET" in
  /*) PROJECT_DIR="$TARGET" ;;
  *) PROJECT_DIR="$(cd "$TARGET" && pwd)" ;;
esac

CLAUDE_DIR="$PROJECT_DIR/.claude"
HOOK_DIR="$CLAUDE_DIR/hooks"
LIB_DIR="$CLAUDE_DIR/lib"

mkdir -p "$HOOK_DIR" "$LIB_DIR"

cp "$ROOT_DIR/adapters/claude-code/lib/agentcloseout-physics-hook.sh" "$LIB_DIR/agentcloseout-physics-hook.sh"
chmod +x "$LIB_DIR/agentcloseout-physics-hook.sh"
cp "$ROOT_DIR/adapters/claude-code/hooks/agentcloseout-tamper-guard.sh" "$HOOK_DIR/agentcloseout-tamper-guard.sh"
chmod +x "$HOOK_DIR/agentcloseout-tamper-guard.sh"

for hook in "${HOOKS[@]}"; do
  case "$hook" in
    no-vibes|no-wrap-up|no-cliffhanger|no-roleplay-drift|no-sycophancy) ;;
    *)
      echo "unknown hook: $hook" >&2
      exit 2
      ;;
  esac
  cp "$ROOT_DIR/adapters/claude-code/hooks/$hook.sh" "$HOOK_DIR/$hook.sh"
  chmod +x "$HOOK_DIR/$hook.sh"
done

cat > "$CLAUDE_DIR/agentcloseout.env" <<EOF
# Generated by AgentCloseoutBench adapters/claude-code/install.sh
AGENTCLOSEOUT_PHYSICS="$ROOT_DIR/bin/agentcloseout-physics"
AGENTCLOSEOUT_RULES="$ROOT_DIR/rules/closeout"
AGENTCLOSEOUT_MODE="enforce"
EOF

write_hook_entries() {
  local first=1 hook
  for hook in "${HOOKS[@]}"; do
    if [ "$first" -eq 0 ]; then
      printf ',\n'
    fi
    first=0
    printf '          {"type": "command", "command": "bash \\"$CLAUDE_PROJECT_DIR/.claude/hooks/%s.sh\\"", "timeout": 10}' "$hook"
  done
}

{
  printf '{\n'
  printf '  "_comment": "Merge these generated hook entries into .claude/settings.json.",\n'
  printf '  "hooks": {\n'
  printf '    "Stop": [\n'
  printf '      {\n'
  printf '        "hooks": [\n'
  write_hook_entries
  printf '\n        ]\n'
  printf '      }\n'
  printf '    ],\n'
  printf '    "PreToolUse": [\n'
  printf '      {\n'
  printf '        "hooks": [\n'
  printf '          {"type": "command", "command": "bash \\"$CLAUDE_PROJECT_DIR/.claude/hooks/agentcloseout-tamper-guard.sh\\"", "timeout": 10}\n'
  printf '        ]\n'
  printf '      }\n'
  printf '    ],\n'
  printf '    "SubagentStop": [\n'
  printf '      {\n'
  printf '        "hooks": [\n'
  write_hook_entries
  printf '\n        ]\n'
  printf '      }\n'
  printf '    ]\n'
  printf '  }\n'
  printf '}\n'
} > "$CLAUDE_DIR/settings.agentcloseout.example.json"

echo "Installed AgentCloseoutBench physics adapters into $CLAUDE_DIR"
echo "Merge $CLAUDE_DIR/settings.agentcloseout.example.json into $CLAUDE_DIR/settings.json"

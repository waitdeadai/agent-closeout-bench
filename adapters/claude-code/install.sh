#!/usr/bin/env bash
# Install AgentCloseoutBench physics-backed Claude Code hook adapters.

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
TARGET="${1:-}"

if [ -z "$TARGET" ] || [ "$TARGET" = "-h" ] || [ "$TARGET" = "--help" ]; then
  echo "Usage: bash adapters/claude-code/install.sh /path/to/project [--profile profile] [hook ...]" >&2
  echo "Hooks: no-vibes no-wrap-up no-cliffhanger no-roleplay-drift no-sycophancy" >&2
  echo "Profiles: solo-lab team-safe ci-static ci-runtime enterprise-managed" >&2
  echo "A PreToolUse tamper guard is installed with every selection." >&2
  exit 2
fi

shift || true

ALL_HOOKS=(no-vibes no-wrap-up no-cliffhanger no-roleplay-drift no-sycophancy)
PROFILE="team-safe"
HOOKS=()

while [ "$#" -gt 0 ]; do
  case "$1" in
    --profile)
      shift || {
        echo "--profile requires a value" >&2
        exit 2
      }
      PROFILE="${1:-}"
      ;;
    --profile=*)
      PROFILE="${1#--profile=}"
      ;;
    --)
      shift
      while [ "$#" -gt 0 ]; do
        HOOKS+=("$1")
        shift
      done
      break
      ;;
    -*)
      echo "unknown option: $1" >&2
      exit 2
      ;;
    *)
      HOOKS+=("$1")
      ;;
  esac
  shift || true
done

if [ "${#HOOKS[@]}" -eq 0 ]; then
  HOOKS=("${ALL_HOOKS[@]}")
fi

case "$TARGET" in
  /*) PROJECT_DIR="$TARGET" ;;
  *) PROJECT_DIR="$(cd "$TARGET" && pwd)" ;;
esac

CLAUDE_DIR="$PROJECT_DIR/.claude"
HOOK_DIR="$CLAUDE_DIR/hooks"
LIB_DIR="$CLAUDE_DIR/lib"
ENGINE="$ROOT_DIR/bin/agentcloseout-physics"
RULES="$ROOT_DIR/rules/closeout"
RULE_PACK_HASH=""
MODE="enforce"
LOOP_GUARD_MODE="release"
LOOP_GUARD_REPAIR_LIMIT="1"
TELEMETRY_MODE="off"
TELEMETRY_QUEUE=""
REQUIRE_CLEAN_ENV="0"

case "$PROFILE" in
  solo-lab)
    MODE="enforce"
    LOOP_GUARD_MODE="release"
    TELEMETRY_MODE="off"
    REQUIRE_CLEAN_ENV="0"
    ;;
  team-safe)
    MODE="enforce"
    LOOP_GUARD_MODE="release"
    TELEMETRY_MODE="off"
    REQUIRE_CLEAN_ENV="0"
    ;;
  ci-static)
    MODE="observe"
    LOOP_GUARD_MODE="release"
    TELEMETRY_MODE="off"
    REQUIRE_CLEAN_ENV="1"
    ;;
  ci-runtime)
    MODE="enforce"
    LOOP_GUARD_MODE="strict"
    TELEMETRY_MODE="minimal_stats"
    TELEMETRY_QUEUE="$CLAUDE_DIR/agentcloseout-telemetry.jsonl"
    REQUIRE_CLEAN_ENV="1"
    ;;
  enterprise-managed)
    MODE="enforce"
    LOOP_GUARD_MODE="strict"
    TELEMETRY_MODE="minimal_stats"
    TELEMETRY_QUEUE="$CLAUDE_DIR/agentcloseout-telemetry.jsonl"
    REQUIRE_CLEAN_ENV="1"
    ;;
  *)
    echo "unknown profile: $PROFILE" >&2
    echo "Profiles: solo-lab team-safe ci-static ci-runtime enterprise-managed" >&2
    exit 2
    ;;
esac

if [ -x "$ENGINE" ] && command -v jq >/dev/null 2>&1; then
  set +e
  RULE_PACK_HASH="$("$ENGINE" lint-rules "$RULES" 2>/dev/null | jq -r '.rule_pack_hash // empty' 2>/dev/null)"
  set -e
fi

mkdir -p "$HOOK_DIR" "$LIB_DIR"

cp "$ROOT_DIR/adapters/claude-code/lib/agentcloseout-physics-hook.sh" "$LIB_DIR/agentcloseout-physics-hook.sh"
chmod +x "$LIB_DIR/agentcloseout-physics-hook.sh"
cp "$ROOT_DIR/adapters/claude-code/hooks/agentcloseout-tamper-guard.sh" "$HOOK_DIR/agentcloseout-tamper-guard.sh"
chmod +x "$HOOK_DIR/agentcloseout-tamper-guard.sh"

for hook in "${HOOKS[@]}"; do
  case "$hook" in
    no-vibes|no-wrap-up|no-cliffhanger|no-roleplay-drift|no-sycophancy) ;;
    *)
      echo "unknown hook: $hook" >&2
      exit 2
      ;;
  esac
  cp "$ROOT_DIR/adapters/claude-code/hooks/$hook.sh" "$HOOK_DIR/$hook.sh"
  chmod +x "$HOOK_DIR/$hook.sh"
done

cat > "$CLAUDE_DIR/agentcloseout.env" <<EOF
# Generated by AgentCloseoutBench adapters/claude-code/install.sh
AGENTCLOSEOUT_PROFILE="$PROFILE"
AGENTCLOSEOUT_PHYSICS="$ENGINE"
AGENTCLOSEOUT_RULES="$RULES"
AGENTCLOSEOUT_RULE_PACK_HASH="$RULE_PACK_HASH"
AGENTCLOSEOUT_MODE="$MODE"
AGENTCLOSEOUT_REQUIRE_CLEAN_ENV="$REQUIRE_CLEAN_ENV"
AGENTCLOSEOUT_LOOP_GUARD_MODE="$LOOP_GUARD_MODE"
AGENTCLOSEOUT_LOOP_GUARD_REPAIR_LIMIT="$LOOP_GUARD_REPAIR_LIMIT"
AGENTCLOSEOUT_LOOP_GUARD_STATE="$CLAUDE_DIR/agentcloseout-loop-guard.jsonl"
AGENTCLOSEOUT_TELEMETRY_MODE="$TELEMETRY_MODE"
AGENTCLOSEOUT_TELEMETRY_QUEUE="$TELEMETRY_QUEUE"
EOF

write_hook_entries() {
  local first=1 hook
  for hook in "${HOOKS[@]}"; do
    if [ "$first" -eq 0 ]; then
      printf ',\n'
    fi
    first=0
    printf '          {"type": "command", "command": "bash \\"$CLAUDE_PROJECT_DIR/.claude/hooks/%s.sh\\"", "timeout": 10}' "$hook"
  done
}

{
  printf '{\n'
  printf '  "_comment": "Merge these generated hook entries into .claude/settings.json.",\n'
  printf '  "_agentcloseout_profile": "%s",\n' "$PROFILE"
  printf '  "hooks": {\n'
  printf '    "Stop": [\n'
  printf '      {\n'
  printf '        "hooks": [\n'
  write_hook_entries
  printf '\n        ]\n'
  printf '      }\n'
  printf '    ],\n'
  printf '    "PreToolUse": [\n'
  printf '      {\n'
  printf '        "hooks": [\n'
  printf '          {"type": "command", "command": "bash \\"$CLAUDE_PROJECT_DIR/.claude/hooks/agentcloseout-tamper-guard.sh\\"", "timeout": 10}\n'
  printf '        ]\n'
  printf '      }\n'
  printf '    ],\n'
  printf '    "SubagentStop": [\n'
  printf '      {\n'
  printf '        "hooks": [\n'
  write_hook_entries
  printf '\n        ]\n'
  printf '      }\n'
  printf '    ]\n'
  printf '  }\n'
  printf '}\n'
} > "$CLAUDE_DIR/settings.agentcloseout.example.json"

echo "Installed AgentCloseoutBench physics adapters into $CLAUDE_DIR"
echo "Profile: $PROFILE"
echo "Merge $CLAUDE_DIR/settings.agentcloseout.example.json into $CLAUDE_DIR/settings.json"
